/// <reference path="../../t6s-core/core/scripts/infotype/Info.ts" />

/**
 * Represents a Renderer of The6thScreen Client.
 *
 * @interface Renderer<RenderInfo extends Info>
 */
interface Renderer<RenderInfo extends Info> {
	/**
	 * Transform the Info list to another Info list.
	 *
	 * @method transformInfo<ProcessInfo extends Info>
	 * @param {ProcessInfo} info - The Info to transform.
	 * @return {Array<RenderInfo>} listTransformedInfos - The Info list after transformation.
	 */
    transformInfo<ProcessInfo extends Info>(info : ProcessInfo) : Array<RenderInfo>;

	/**
	 * Render the Info in specified DOM Element.
	 *
	 * @method render
	 * @param {RenderInfo} info - The Info to render.
	 * @param {DOM Element} domElem - The DOM Element where render the info.
	 * @param {string} rendererTheme - The Renderer's theme.
	 * @param {Function} endCallback - Callback function called at the end of render method.
	 */
    render(info : RenderInfo, domElem : any, rendererTheme : string, endCallback : Function);

	/**
	 * Update rendering Info in specified DOM Element.
	 *
	 * @method updateRender
	 * @param {RenderInfo} info - The Info to render.
	 * @param {DOM Element} domElem - The DOM Element where render the info.
	 * @param {string} rendererTheme - The Renderer's theme.
	 * @param {Function} endCallback - Callback function called at the end of updateRender method.
	 */
	updateRender(info : RenderInfo, domElem : any, rendererTheme : string, endCallback : Function);

	/**
	 * Animate rendering Info in specified DOM Element.
	 *
	 * @method animate
	 * @param {RenderInfo} info - The Info to animate.
	 * @param {DOM Element} domElem - The DOM Element where animate the info.
	 * @param {string} rendererTheme - The Renderer's theme.
	 * @param {Function} endCallback - Callback function called at the end of animation.
	 */
	animate(info : RenderInfo, domElem : any, rendererTheme : string, endCallback : Function);
}